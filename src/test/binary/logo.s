.global _start
_start:
    lsi a1, 16
    li a2, 16+32
    lsi a3, -1

loop:
    lw a0, 0(a1)
    xor2 a0, a3    # not a0
    sw a0, 0(a1)
    addi2 a1, 2
    blt a1, a2, loop

    hlt

.data
    .zero 16-2
    .2byte 0b0000000100000000
    .2byte 0b0000000100000000
